#input จำนวนเงินเข้ามา
money = float(input("Enter money"))
#process เมื่อรับจำนวนเงินเข้ามาทางคีบอร์ดแล้วให้นำมาคิดภาษีโดยมีภาษีเท่ากับ 7 %
#Define a variable กำหนดให้ตัวแปรทั้งหมดเป็นข้อมูลแบบ float
tax = float(money*(7/100))
#output เมื่อทำขั้นตอน process แล้วก็ทำการ output คำตอบออกมาด้วยคำสั่ง print
print("tax =",tax)


